def parse_user_input(text):
    translations = {}
    lines = text.strip().split('\n')
    for line in lines:
        line = line.strip()
        if not line or line.startswith('//'):
            continue
        match = re.match(r'"([^"]+)"\s*=\s*"([^"]+)"', line)
        if match:
            key, val = match.groups()
            translations[key] = val
    return translations

def parse_app_strings(content):
    # This is a bit tricky due to the multi-line dictionary
    # We'll use a regex to find the 'all' dictionary content
    match = re.search(r'static let all: \[String: \[String: String\]\] = \[(.*?)\]\s*\}', content, re.DOTALL)
    if not match:
        return None, None
    dict_content = match.group(1)
    
    entries = {}
    # Find entries like "key": ["de": "...", "en": "..."],
    pattern = r'"([^"]+)":\s*\[(.*?)\]'
    for key, inner_dict_str in re.findall(pattern, dict_content, re.DOTALL):
        inner_dict = {}
        # Find inner keys like "de": "..."
        inner_pattern = r'"([^"]+)":\s*"([^"]*)"'
        for lang, val in re.findall(inner_pattern, inner_dict_str):
            inner_dict[lang] = val
        entries[key] = inner_dict
    
    return entries, match.start(1), match.end(1)

